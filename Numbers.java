package com.guvi.task1;

public class Numbers {
	
	public static void main(String[] args) {	
		//Numbers from 10 to 50 using for loop
		for(int i=10;i<=50;i++)
		{
			System.out.print(i+" ");
		}
		//Numbers from 10 to 50 using while loop
		int i=10;
		while(i>0)
		{
			System.out.print(i+" ");
			i++;
		}
	}
}
